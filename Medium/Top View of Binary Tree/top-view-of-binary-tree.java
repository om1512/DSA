class Solution
{
    static class Block{
        int depth;
        int val;
        Block(){}
        Block(int depth, int val){
            this.depth = depth;
            this.val = val;
        }
    }
    static TreeMap<Integer, Block> solve(Node root, int vertical, int depth, TreeMap<Integer, Block> map){
        if(root == null) return null;
        if(map.containsKey(vertical)){
            Block b = map.get(vertical);
            if(depth < b.depth){
                b.val = root.data;
                b.depth = depth;
                map.put(vertical, b);
            }
        }else{
            Block b = new Block();
            b.depth = depth;
            b.val = root.data;
            map.put(vertical, b);
        }
        solve(root.left, vertical-1, depth+1, map);
        solve(root.right, vertical+1, depth+1, map);
        return map;
    } 
    static ArrayList<Integer> topView(Node root)
    {
        TreeMap<Integer, Block> map = solve(root, 0,0,new TreeMap<>());
        ArrayList<Integer> list = new ArrayList<>();
        map.forEach((key, value) -> {
            list.add(value.val);
        });
        return list;
    }
}